#[cfg(test)]
mod tests {
    use super::*;
    use crate::backend::mock::MockBackend;
    use crate::command::RawOutput;

    fn make_inner(responses: Vec<RawOutput>) -> HostInner {
        HostInner {
            backend: Box::new(MockBackend::new(responses)),
            runtime: tokio::runtime::Runtime::new().unwrap(),
            connection_string: "mock://".to_owned(),
        }
    }

    #[test]
    fn test_parse_tcp() {
        let spec = parse_socket_spec("tcp://0.0.0.0:22").unwrap();
        assert!(
            matches!(spec, SocketSpec::Tcp { ref address, port } if address == "0.0.0.0" && port == 22)
        );
    }

    #[test]
    fn test_parse_udp() {
        let spec = parse_socket_spec("udp://127.0.0.1:53").unwrap();
        assert!(
            matches!(spec, SocketSpec::Udp { ref address, port } if address == "127.0.0.1" && port == 53)
        );
    }

    #[test]
    fn test_parse_unix() {
        let spec = parse_socket_spec("unix:///var/run/docker.sock").unwrap();
        assert!(matches!(spec, SocketSpec::Unix { ref path } if path == "/var/run/docker.sock"));
    }

    #[test]
    fn test_parse_unknown_scheme() {
        let err = parse_socket_spec("http://localhost:80").unwrap_err();
        assert!(err.to_string().contains("unknown socket scheme"));
    }

    #[test]
    fn test_parse_missing_port() {
        let err = parse_socket_spec("tcp://0.0.0.0").unwrap_err();
        assert!(err.to_string().contains("missing port"));
    }

    #[test]
    fn test_parse_empty_unix_path() {
        let err = parse_socket_spec("unix://").unwrap_err();
        assert!(err.to_string().contains("empty unix socket path"));
    }

    #[test]
    fn test_is_listening_tcp_found() {
        let inner = make_inner(vec![RawOutput {
            rc: 0,
            stdout:
                b"LISTEN  0  128  0.0.0.0:22  0.0.0.0:*\nLISTEN  0  128  0.0.0.0:80  0.0.0.0:*\n"
                    .to_vec(),
            stderr: vec![],
        }]);
        assert!(
            inner
                .runtime
                .block_on(is_listening_impl(&inner, "tcp://0.0.0.0:22"))
                .unwrap()
        );
    }

    #[test]
    fn test_is_listening_tcp_not_found() {
        let inner = make_inner(vec![RawOutput {
            rc: 0,
            stdout: b"LISTEN  0  128  0.0.0.0:80  0.0.0.0:*\n".to_vec(),
            stderr: vec![],
        }]);
        assert!(
            !inner
                .runtime
                .block_on(is_listening_impl(&inner, "tcp://0.0.0.0:22"))
                .unwrap()
        );
    }

    #[test]
    fn test_is_listening_unix_found() {
        let inner = make_inner(vec![RawOutput {
            rc: 0,
            stdout: b"u_str  LISTEN  0  128  /var/run/docker.sock  12345  * 0\n".to_vec(),
            stderr: vec![],
        }]);
        assert!(
            inner
                .runtime
                .block_on(is_listening_impl(&inner, "unix:///var/run/docker.sock"))
                .unwrap()
        );
    }

    #[test]
    fn test_is_listening_unix_not_found() {
        let inner = make_inner(vec![RawOutput {
            rc: 0,
            stdout: b"u_str  LISTEN  0  128  /var/run/other.sock  12345  * 0\n".to_vec(),
            stderr: vec![],
        }]);
        assert!(
            !inner
                .runtime
                .block_on(is_listening_impl(&inner, "unix:///var/run/docker.sock"))
                .unwrap()
        );
    }
}
